package Pattern_Java;

public class Pattern_Alpha 
{
	public static void main(String[] args) {
		
//	//
//		A B C D E 
//		A B C D E 
//		A B C D E 
//		A B C D E 
//		A B C D E 
	
	for(int i=1;i<=5;i++)
	{
		char ch='A';
		for(int j=1;j<=5;j++)
		{
			System.out.print(ch++ +(" "));
			
		}
	
	System.out.println();
	}	
}
}



